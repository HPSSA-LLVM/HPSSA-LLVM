#include<bits/stdc++.h>
using namespace std;

int main()
{

  int a, b; 
  int z;    

  if (b > 50) {
    a = 50;
  }

  if (a > 50) {
    b = 50;
  }  else  {
    a = 0;
    b = 0;
  }

  if (a >= b) {
    a = a + z;
  }  else  {
    b = b + z;
  }

  if (a <= 2 * b) {
    a = a + 2 * z;
  }  else {
    b = 0;
  }

  return 0;
}
